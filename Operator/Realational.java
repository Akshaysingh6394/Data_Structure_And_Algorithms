public class Realational {
    public static void main(String[] args) {
        int A = 6;
        int B = 9;
        System.out.println(A==B);
        System.out.println(A!=B);
        System.out.println(A>B);
        System.out.println(A<B);
        System.out.println(A>=B);
        System.out.println(A<=B);
    }
    
}
