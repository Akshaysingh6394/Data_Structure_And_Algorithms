public class Assignment {
    public static void main(String[] args) {
        int A = 4;
        int B = 8;
        System.out.println(A>B);
        System.out.println(A<B);
        System.out.println(A+=B);
        System.out.println(A=A+B);
        System.out.println(A*=B);
        System.out.println(A=A*B);
        System.out.println(A/=B);
        System.out.println(A/=B);
// we use assignment operator only saving time and space we can write both type but when we write large code then realise these type opf problem solving


        
    }
    
}
